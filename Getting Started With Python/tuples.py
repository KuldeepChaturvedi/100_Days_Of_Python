#!/usr/bin/python

# Tuple is immutable
# Tuple does not allow to change values stored in it
# Tuple declared in round brackets

tup = (45,32,33,25)

# This will print the tuple tup
print('--------'*4)
print('Printing the all values in tuple.')
print(tup)

# This will print the 1 index tuple value
print('--------'*4)
print('Printing index 1 tuple value.')
print(tup[1])


